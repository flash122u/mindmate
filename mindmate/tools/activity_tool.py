class RandomActivityTool(Tool):
    """推荐一件随手能做的小事."""

    name = "get_random_activity"
    description = (
        "推荐一件随手能做的治愈小事。用户说'好无聊''不知道干嘛''好没劲'"
        "'有什么好玩的''无聊死了'时用这个。可以指定类别：放松、动一动、创造、吃点啥、连接、微小快乐。"
    )
    parameters = {
        "type": "object",
        "properties": {
            "category": {
                "type": "string",
                "description": (
                    f"活动类别，可选：{'、'.join(_ACTIVITIES.keys())}。"
                    "不填则随机选择。根据用户的心情选择合适的类别，"
                    "比如累了选'放松'，无聊选'创造'或'动一动'，"
                    "想吃东西选'吃点啥'，想和人说话选'连接'。"
                ),
            }
        },
    }

    async def execute(self, category: str = "", **_: object) -> str:
        category = (category or "").strip()
        # 按类别筛选
        if category and category in _ACTIVITIES:
            pool = _ACTIVITIES[category]
        else:
            # 随机选一个类别
            pool = random.choice(list(_ACTIVITIES.values()))
        # 随机选一条
        activity = random.choice(pool)
        # 告诉 LLM 从哪里选的，方便它自然地嵌入对话
        cat_name = category if (category and category in _ACTIVITIES) else "随机"
        return f"[{cat_name}] {activity}"
